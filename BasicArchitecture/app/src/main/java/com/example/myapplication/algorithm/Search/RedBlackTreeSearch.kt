package com.example.myapplication.algorithm.Search

class RedBlackTreeSearch {
//    1根节点是黑色的
//    2每个叶子节点都是黑色的空节点，也就是说，叶子节点不存储数据
//    3任何相邻的节点都不能同时为红色，也就是说，红色节点是被黑色节点隔开的(也就是说如果一个节点是红色的，那么它的子节点必须是黑色的，也就是不能出现两个红色的结点，两个连续的黑色节点是可能出现的)
//    4每个节点，从该节点到达其可达叶子节点的所有路径，都包含相同数目的黑色节点

     //黑色表示普通节点，红色表示2节点
     //叶子节点为黑色并不存储数据 是为了方便  代码实现
     //3,4就是为了保持平衡，也就是左子树跟右子树高度差距不要太大，左旋右悬和变换颜色也就是为了保持3,4的要求
}